<h3 id="standardsCompliance">Standards compliance</h3>

<p>All pages comply with priority 1 guidelines of the <a href="http://www.w3.org/TR/WCAG10/">W3 Web Content Accessibility Guidelines</a> and the <acronym title="United States">U.S.</acronym> Federal Government <a href="http://www.section508.gov/">Section 508 Guidelines</a>.</p>

<p>All pages validate as <abbr title="eXtensible HyperText Language version 1">XHTML 1</abbr> Strict. They use structured semantic markup.</p>

<h3 id="links">Links</h3>

<p>Many links have title attributes which describe the link in greater detail, unless the text of the link already fully describes the target (such as the headline of an article).</p>

<p>Whenever possible, links are written to make sense out of context. Many browsers (such as <acronym>JAWS</acronym>, Home Page Reader, Lynx, and Opera) can extract the list of links on a page and allow the user to browse the list, separately from the page. To aid this, link text is never duplicated; two links with the same link text always point to the same address.</p>

<p>There are no <code>javascript:</code> pseudo-links.  All links can be followed in any browser, even if scripting is turned off. There are no links that open new windows without warning.</p>

<h3 id="visualDesign">Visual design</h3>

<p>This site use cascading style sheets for visual layout. If your browser or browsing device does not support stylesheets at all, the content of each page is still readable.</p>

<p>The layout is completely liquid, simply filling its window. It happily accommodates resizing text and, as relative units have been used, text can even be re-sized in Internet Explorer for Windows.</p>


