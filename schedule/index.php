<span class="summary">Web Services: Fuelling Innovation and&nbsp;Entrepreneurship</span>
<span class="speaker">by
<a class="organizer" href="../speakers/#barr">Jeff Barr</a>
</span>
</h3>

<div class="description">

<div class="presentation-info">
<p><a href="http://2006.dconstruct.org/podcast/jeffbarr.mp3">Download Jeff&#39;s mp3</a></p>
<p></p>
