<p>Web services are changing the fundamental nature of the web, as more and more companies offer their data for free. Rather than spending millions of dollars on complicated systems, entrepreneurs can tap into the existing services of companies like Amazon, and create innovative new enterprises for a fraction of the cost; enterprises that wouldn&#8217;t have been economical otherwise.</p>

<p>In this session, Amazon Web Services Evangelist, Jeff Barr, will discuss the power of open APIs and how they are helping to fuel innovation and entrepreneurship. Jeff will discuss Amazon&#8217;s motivation for building AWS and some of the design decisions (such as their use of XSLT) they made along the way. Jeff will touch on some of Amazon&#8217;s current offerings such as S3 and the Mechanical Turk, before showing demonstrations of how these services are being used in the wild.</p>

