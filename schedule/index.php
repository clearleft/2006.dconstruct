<p>Over the course of d.Construct, you&#8217;re going to hear plenty about <abbr title="Application Programming Interface">API</abbr>s from the people providing them: Yahoo!, Amazon, etc. But why should you, as a developer, be interested?</p>

<p>Come on a journey with Jeremy Keith as he describes how much fun can be had from hacking around with open data. Listen to his experiences of experimenting with mashups. Find out how Web Services can rekindle the passion in your code.</p>

<p>After some initial foreplay describing the differences between <abbr title="Representational State Transfer">REST</abbr> and <abbr title="Snakes On A Plane">SOAP</abbr>, join Jeremy as he penetrates some code. Soon you&#8217;ll be swinging with Amazon, Flickr, and Google Maps.</p>
