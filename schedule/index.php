<p>How can we build modern web applications that use DOM Scripting and Ajax-type technologies and ensure that they are accessible?</p>

<p>To find the answers Derek will look at the impact of Ajax and dynamically-generated content on people with disabilities by examining how various assistive technologies interact with modern web development techniques such as DOM Scripting and Ajax. Using those results Derek will create a strategy to make some currently popular design patterns more accessible to all users.</p>
