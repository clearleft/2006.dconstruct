<span class="summary">Accessible Web Applications in a Post Web&nbsp;1.0&nbsp;World</span>
<span class="speaker">by
<a class="organizer" href="../speakers/#featherrstone">Derek Featherstone</a>
</span>
</h3>

<div class="description">

<div class="presentation-info">
