<p>Can you ever go back to Ajax once you Web 2.0 with Flex 2.0?</p>

<p>The Flex 2 framework and the Eclipse-based Flex Builder 2 IDE provide you with a superior development workflow for creating web applications. You can create rich user interfaces quickly by using features such as data binding, application states, custom components, effects, and transitions.</p>

<p>Join Aral Balkan, the Lone Ranger of the Flash Platform at d.Construct, as he shows you how easy it is to use open data, consume web services and create mashups in Flex 2 by using open source ActionScript 3 libraries for Flickr, Mappr, Odeo, and YouTube.</p>

