<p>Over the last year the Yahoo! Developer Network has opened up dozens of sites and services to external software developers, with APIs for Yahoo! Search, Flickr, del.icio.us, Yahoo! Maps, and many others. More recently Yahoo! has started adopting microformats on Yahoo! Local and upcoming.org.</p>

<p>Simon and Paul will be peeking behind the Yahoo! firewall, showing how these services are created and discussing some of the lessons learned in releasing them to the public.  They will also show how a company can make use of web services internally to solve real-world technical problems, encourage innovation, and make work more enjoyable.</p>

